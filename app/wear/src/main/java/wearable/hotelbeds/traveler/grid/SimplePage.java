public class SimplePage {

    public String mPrice;
    public String mCalendar;
    public String mHotel;
    public String mFlyOut;
    public String mFlyIn;

    public SimplePage(String price, String calendar, String hotel, String flyout, String flyin) {
        this.mPrice = price;
        this.mCalendar = calendar;
        this.mHotel = hotel;
        this.mFlyOut = flyout;
        this.mFlyIn = flyin;
    }

}

